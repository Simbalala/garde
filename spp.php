      <h2>Sapeur-Pompier Professionel</h2>


      <p>Les sapeurs-pompiers professionnels sont des fonctionnaires territoriaux qui exercent au sein des Services départementaux d'incendie et de secours. Leur recrutement se fait par voie de concours.</p>

      <p>Deux niveaux sont proposés :</p>

      <p>Sapeurs-pompiers non officiers :</p>
      <p>Ces concours sont organisés par les services départementaux d'incendie et de secours. Ils ont lieu soit en concours externe soit en concours réservé aux sapeurs-pompiers volontaires.<br>
      Ce cadre d'emplois comprend les grades de sapeur, caporal, sergent et adjudant.</p>

      <p>Lieutenants (officiers) :</p>

      <p>Ce concours est organisé par le Ministère de l'Intérieur, de l'Outre-mer et des collectivités territoriales (en concours externe et interne ). Il est également possible d'accéder à ce grade par l'obtention d'un examen professionnel.<br>
      La réussite à l'un de ces concours permettra au lauréat d'être inscrit sur une liste d'aptitude. Il pourra alors faire acte de candidature, en fonction du concours obtenu, sur un emploi vacant dans tous les services départementaux d'incendie et de secours.<br>
      Dans le cas où il n'aurait pas trouvé de poste dans un délai de trois ans, le candidat devra concourir à nouveau.</p>

