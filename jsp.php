      <h2>Jeune Sapeur-Pompier</h2>

      <h2>Conditions d''inscription :</h2>

      <ul>
        <li><p>être âgé de 12 à 14 ans,</p></li>
        <li><p>être apte physiquement  certificat médical d'aptitude,</p></li>
        <li><p>physique délivré par un médecin sapeur-pompier,</p></li>
        <li><p>fournir une attestation parentale,</p></li>
        <li><p>cotiser à une association de JSP du département.</p></li>
      </ul>

      <h2>Devenir Jeune sapeur-pompier, c'est :</h2>

      <p>- Apprendre les bases des techniques d'interventions lors d'incendies, de sauvetage, d'opérations diverses, <br>

      - Se former aux gestes de secours à la personne en obtenant son Prompt Secours, <br>

      - Pratiquer le sport, cross, athlétisme et Parcours Sportif Sapeur-Pompier, <br>

      - Participer à des rassemblement départementaux, régionaux, voire nationaux, <br>

      - Apprendre la discipline, le civisme, la solidarité et le respect d'autrui, <br>

      - Obtenir son Brevet National de JSP et ainsi pouvoir devenir Sapeur-pompier volontaire ou professionnel.</p>

