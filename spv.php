    <h2>Sapeur-Pompier Volontaire</h2>
    <p>Pour vous engager en qualité de sapeur-pompier volontaire (SPV), vous devez vous rapprocher du chef du centre d'incendie et de secours le plus proche de chez vous. Vous découvrirez ainsi le quotidien d'un sapeur-pompier volontaire, ses missions, son engagement et les conditions nécessaires pour faire partie de la famille des sapeurs-pompiers.</p>

    <p>Pour être sapeur-pompier volontaire, il faut :</p>

    <p>- être âgé de 16 à 55 ans <br>

    - remplir les conditions d’aptitude médicale et physique nécessaires <br>

    - jouir de ses droits civiques <br>

    - être en situation régulière par rapport au service national <br>

    - avoir une autorisation parentale pour les moins de 18 ans.</p>

    <p>Les pompiers volontaires sont engagés pour cinq ans. Leur engagement est renouvelé par tacite reconduction, mais est subordonné à la vérification périodique des conditions d’aptitude physique et médicale.</p>

    <p>Le sapeur-pompier volontaire intervient en équipe sur les interventions de son secteur. Il peut ainsi être amené à secourir des personnes, éteindre des incendies, porter assistance pour des opérations diverses.</p>
