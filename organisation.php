    <h2>Organisation du SDIS</h2>
    <p>Le Service Départemental d'Incendie et de Secours est un établissement public administratif, compétent au niveau départemental pour assurer les missions de sécurité civile.<br>

    <p>Une responsabilité partagée par :</p>

    <p>- le Président du Conseil d'Administration, responsable de la gestion administrative et financière du service,<br>
    - le Préfet et les Maires, responsables de la sécurité à l'échelle de du département ou de leur commune.</p>

    <h2>Administration et financement</h2>

    <p>Le SDIS est géré par un conseil d'administration, composé de 16 conseillers départementaux, de 6 représentants des communes et présidé par le Président du Conseil Départemental.</p>
